# http://www.checkio.org/mission/open-labyrinth


def checkio(maze):

    DIRECTIONS = {"E": (1, 0), "W": (-1, 0), "N": (0, -1), "S": (0, 1)}

    def neighbors_and_directions(position):
        x, y = position
        neighbors_and_directions = []

        for d in DIRECTIONS:
            # neighbor = tuple(map(sum, zip(position, DIRECTIONS[d]))
            dx, dy = DIRECTIONS[d]

            if maze[y+dy][x+dx] == 0:
                neighbors_and_directions.append(((x+dx, y+dy), d))

        return neighbors_and_directions

    # Use BFS
    queue = [((1, 1), "")]
    visited = set()

    while queue:
        current_position, route = queue.pop(0)

        if current_position == (10, 10):
            return route

        if current_position in visited:
            continue

        for neighbor, direction in neighbors_and_directions(current_position):
            if neighbor not in visited:
                queue.append((neighbor, route+direction))
        visited.add(current_position)


